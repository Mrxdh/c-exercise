#include <stdio.h>
/* Jone算出结果应该怎么写函数？ */
int joneResult(int x, int y, int z) {
  int sum = x + y + z;
  return sum; //这里是不是应该将sum返回呢？
}
/* Jack没有算出结果只说了一句话是不是应该用无返回值函数？ */
void jackResult(int x, int y, int z) { printf("我算不出来\n"); }
int main() {
  int a, b, c;
  a = 10;
  b = 20;
  c = 30;
  int jR = joneResult(a, b, c);
  printf("Jone运算结果是：%d\n", jR / 3);
  printf("Jack运算结果是：");
  // Jack没有算出结果只说了句话，是不是直接调用函数就可以了？
  jackResult(1, 2, 3);
  return 0;
}
